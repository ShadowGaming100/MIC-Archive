print("This code calculates the area of square with keyboard input.")

# Prompt the user to enter/input the length of the square.
length = int(input("Enter/Input the length of the square: "))

# Prompt the user to enter/input the width of the square.
width = int(input("Enter/Input the width of the square: "))

# Calculate the area of the square.
area = length * width

# Print the area of the square.
print("The area of square is:", area)